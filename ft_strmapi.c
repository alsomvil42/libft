#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int				len;
	unsigned int	i;
	char			*tab;

	len = 0;
	i = 0;
	while (s[len])
		len++;
	len++;
	if (!(tab = malloc(sizeof(char) * len)))
		return (NULL);
	while (s[i])
	{
		tab[i] = f(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
